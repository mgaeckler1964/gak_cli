static void adapt( const STRING &left, const STRING &right )
{
	bool	leftExists = exists(left);
	bool	rightExists = exists(right);

	if( !leftExists && !rightExists )
	{
		throw CmdlineError(left + "and " + right + " not found" );
	}
	if( !leftExists )
		fcopy( right, left );
	else if( !rightExists )
		fcopy( left, right );
	else
	{
		DirectoryEntry leftEntry( left );
		DirectoryEntry rightEntry( right );
		if( leftEntry.modifiedDate < rightEntry.modifiedDate )
			fcopy( right, left );
		else if( leftEntry.modifiedDate > rightEntry.modifiedDate )
			fcopy( left, right );
		else
			std::cout << "No change" << std::endl;
	}
}

