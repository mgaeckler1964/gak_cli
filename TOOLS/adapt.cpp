int main( int argc, const char *argv[] )
{
	int result = EXIT_FAILURE;

	doEnableLogEx(gakLogging::llInfo);
	doImmediateLog();
	doEnterFunctionEx(gakLogging::llInfo,"main");

	try
	{
		if( argc != 3 )
			throw CmdlineError("Missing left and right");

		const char *left = argv[1];
		const char *right = argv[2];
		adapt( left, right );
		result = EXIT_SUCCESS;
	}
	catch( CmdlineError &e )
	{
		std::cerr << argv[0] << ": " << e.what() << std::endl;
		std::cerr << "Usage: " << argv[0] << " <left> <right>" << std::endl;
	}
	catch( std::exception &e )
	{
		std::cerr << argv[0] << ": " << e.what() << std::endl;
	}
	catch( ... )
	{
		std::cerr << argv[0] << ": Unkown error" << std::endl;
	}

	doFlushLogs();
	doDisableLog();
	return result;
}

